import turtle as t

# déplace la tortue aux coordonnées
t.penup()
t.goto(-100,0)
t.pendown()
t.hideturtle() # on cache la tortue
t.speed(0) # on accélère la tortue
t.color('blue')
t.pensize(3)
t.forward(100)
t.left(60)
t.forward(100)
t.right(120)
t.forward(100)
t.left(60)
t.forward(100)